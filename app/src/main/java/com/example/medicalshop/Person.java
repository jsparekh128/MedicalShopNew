package com.example.medicalshop;

public class Person {
    public String pName;
    public String pEmail;
    public String pMobile;
    public String pPassword;
    public Person(){

    }

    public Person(String pName, String pEmail, String pMobile, String pPassword) {
        this.pName = pName;
        this.pEmail = pEmail;
        this.pMobile = pMobile;
        this.pPassword = pPassword;
    }
}
